class Cell{
    constructor(traversable, tileType, row, col) {
        this.traversable = traversable;
        this.tileType = tileType;
        this.row = row;
        this.col = col;
    }
}

class Grid {
    constructor(rows, cols, path) {
        this.rows = rows;
        this.cols = cols;
        this.contents = []; // initialize the rows

        // Populate the Grid with walls and paths
        for(let row = 0; row < this.rows; row++) {
            this.contents[row] = []; // initialize the column 
            for(let col = 0; col < this.cols; col++) {
                // Tile is grass
                if(path[row][col] == 0) 
                {
                    this.contents[row][col] = new Cell(true, 0, row, col);
                } 
                // Tile is a barn
                else if(path[row][col] == 1)
                {
                    this.contents[row][col] = new Cell(true, 1, row, col);
                }
                // Tile is a river
                else if(path[row][col] == 2) 
                { 
                    this.contents[row][col] = new Cell(false, 2, row, col);
                }
                // Tile is a bridge
                else if(path[row][col] == 3)
                {
                    this.contents[row][col] = new Cell(true, 3, row, col);
                }
                // Tile is a mine-area
                else if(path[row][col] == 4)
                {
                    this.contents[row][col] = new Cell(false, 4, row, col);
                }
            }
        }
    }
}

